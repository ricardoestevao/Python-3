"""
Operadores de atribuição
= += -= *= /= //= **= %= (Acontece com números e strings.)
"""
contador = 10

###
contador = contador + 1 # mesma coisa de contador += 1
print(contador)

contador = contador - 1 # = Mesma coisa de contador -= 1
print(contador)

contador = contador * 2 # Mesma coisa de contador *= 2
print(contador)

contador /= 5
print(contador) 

contador = contador ** 2 # Mesmo coisa de **= 2
print(contador)

contador = contador % 2 # Mesma coisa de %= 2 
print(contador)

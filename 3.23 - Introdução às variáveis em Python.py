# Variáveis são usadas para salvar algo na memória do computador. 
# PEP8: inicie variáveis com letras minúsculas, ppode usar.
# números e underline _
# O sinal de = é o operador de atribuição. Ele é usado para atrivuir um valor a um nome (variável)
# Uso: nome_varialvel = expressão
nome_completo = 'Ricardo Santos Estevão da Silva' # Variável é um nome que se refere a um valor armazenado na memória do computador.
soma_dois_mais_quatro = 2 + 4 # Variável pode ser nomeada com letras, números e underline, mas não pode começar com número.
idade = 3 # Variável pode ser nomeada com letras, números e underline, mas não pode começar com número.
maior_de_idade = idade >= 18 # maior_de_idade é uma variável booleana que verifica se a idade é maior ou igual a 18.

print(f'Nome:{nome_completo}, Idade: {idade}') # Exibe o nome completo e a idade.
print(f'É maior de idade? {maior_de_idade}') # Exibe se a pessoa é maior de idade ou não.


# IF / ELIF..... / ELSE
# SE / SE NÃO SE / SE NÃO

condicao = True # True ou False

if condicao: # se a condição for verdadeira
    print('Este código é o if') # executa este bloco de código
elif not condicao: # se a condição for falsa
    print('Este código é o elif') # executa este bloco de código
# se nenhuma das condições anteriores for verdadeira, executa o bloco abaixo
else:
    print('Este código é o else')
print(12, 34, sep='-') # Separador entre os números
print('Ricardo', 'Estevao', sep='-') # Separar entre os nomes
print(56, 78)

# Asplas simples
print('Ricardo Estevão da Silva')

# Asplas duplas
print("Ricardo Santos Estevão da Silva")

# Escape
print("Luiz \"Ricardo\"") # \ é o caracter de escape onde o " é interpretado como texto e não como fim da string

# Outra maneira
print("Ricardo 'Estevão' ") # Posso colocar aspas simples dentro da aspas duplas sem que o interpretador do Python entenda como texto e não fim da string. 